const getListData = (value: Dayjs) => {
    const activitiesForThisDay = mock.filter((activity) =>
        dayjs(activity.startTime).isSame(value, "day"),
    );
    return activitiesForThisDay;
};

const dateCellRender = (value: Dayjs) => {
    const listData = getListData(value);

    if (listData.length === 0) return null;

    return (
        <ul>
            {listData.map((item) => (
                <li
                    key={item._id}
                    onClick={() => alert(`clicked ${item.title}`)}
                >
                    <Card size="small">
                        <Card.Meta title={item.title} />
                        <Flex vertical>
                            <span>
                                {dayjs
                                    .duration(item.durationSec, "seconds")
                                    .format("HH:mm:ss")}
                            </span>
                            <span>
                                {(item.distanceMeters / 1000).toFixed(1)} km
                            </span>
                            <span>{item.summary.tss} TSS</span>
                        </Flex>
                    </Card>
                </li>
            ))}
        </ul>
    );
};

const cellRender: CalendarProps<Dayjs>["cellRender"] = (current, info) => {
    if (info.type === "date") {
        return dateCellRender(current);
    }

    return info.originNode;
};

const CalendarModule = () => {
    const [isDatePickerOpen, setIsDatePickerOpen] = useState(false);

    return (
        <ConfigProvider
            locale={enGB}
            theme={{
                components: {
                    Calendar: {},
                },
            }}
        >
            <Calendar
                cellRender={cellRender}
                headerRender={({ value, onChange }) => (
                    <div>
                        <Space size="middle">
                            <div
                                style={{
                                    position: "relative",
                                    display: "inline-flex",
                                    alignItems: "center",
                                }}
                            >
                                <Space
                                    onClick={() => setIsDatePickerOpen(true)}
                                    style={{
                                        cursor: "pointer",
                                        fontSize: "16px",
                                        fontWeight: 500,
                                        width: "150px",
                                    }}
                                >
                                    <span>{value.format("MMMM YYYY")}</span>
                                    <DownOutlined
                                        style={{
                                            transition: "0.3s",
                                            transform: isDatePickerOpen
                                                ? "rotate(180deg)"
                                                : "none",
                                        }}
                                    />
                                </Space>
                                <DatePicker
                                    open={isDatePickerOpen}
                                    onOpenChange={(open) =>
                                        setIsDatePickerOpen(open)
                                    }
                                    onChange={(date) => {
                                        if (date) {
                                            onChange(date);
                                        }
                                        setIsDatePickerOpen(false);
                                    }}
                                    style={{
                                        position: "absolute",
                                        top: "100%",
                                        left: 0,
                                        visibility: "hidden",
                                        width: 0,
                                        height: 0,
                                        padding: 0,
                                        border: "none",
                                    }}
                                />
                            </div>
                            <Button
                                variant="outlined"
                                onClick={() => onChange(dayjs())}
                            >
                                Today
                            </Button>
                            <Button
                                icon={<LeftOutlined />}
                                onClick={() =>
                                    onChange(value.clone().subtract(1, "month"))
                                }
                            />
                            <Button
                                icon={<RightOutlined />}
                                onClick={() =>
                                    onChange(value.clone().add(1, "month"))
                                }
                            />
                        </Space>
                        <Divider />
                    </div>
                )}
            />
        </ConfigProvider>
    );
};

export default CalendarModule;
